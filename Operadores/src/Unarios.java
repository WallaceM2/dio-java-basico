public class Unarios {
    public static void main(String [] args) {
             // (+) Operador unario valor positivo - numeros positivos sem esse operador explicitamente;
            // (-) Operador unario valor negativo - nega um numero ou expressão aritmeticas;
            // (++) Operador de incremento de valor - adicionar valores
            // (--) Operador de decremento de valor - diminuir valores
            // (!) Operador unario logico de negação - nega o valor de uma expressão ou booleana;

            int numero = 5;

            numero = - numero;

            System.out.println(numero);

            numero = numero *-1; // operador * -1 para deixar o resultado positivo
            
            System.out.println(numero);
    }
}
